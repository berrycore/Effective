package chapter23_generic_do_not_use_raw_type;

public class Coin {

    private String name;
    private int serial;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getSerial() {
        return serial;
    }

    public void setSerial(int serial) {
        this.serial = serial;
    }
}
